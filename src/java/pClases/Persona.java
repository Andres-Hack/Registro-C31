package pClases;

public class Persona {
   
    private String per_ci;
    private String per_paterno;
    private String per_materno;
    private String per_nombre;
    private String per_telefono;      

    public String getPer_ci() {
        return per_ci;
    }

    public void setPer_ci(String per_ci) {
        this.per_ci = per_ci;
    }

    public String getPer_paterno() {
        return per_paterno;
    }

    public void setPer_paterno(String per_paterno) {
        this.per_paterno = per_paterno;
    }

    public String getPer_materno() {
        return per_materno;
    }

    public void setPer_materno(String per_materno) {
        this.per_materno = per_materno;
    }

    public String getPer_nombre() {
        return per_nombre;
    }

    public void setPer_nombre(String per_nombre) {
        this.per_nombre = per_nombre;
    }

    public String getPer_telefono() {
        return per_telefono;
    }

    public void setPer_telefono(String per_telefono) {
        this.per_telefono = per_telefono;
    }
    
        
}